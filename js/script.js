function resistTot() {
    let resistTot;
    let brand1 = document.getElementById("brand1").style.backgroundColor;
    if (brand1) {
        switch (brand1) {
            case "black":
                resistTot = "0";
                break;
            case "brown":
                resistTot = "1";
                break;
            case "red":
                resistTot = "2";
                break;
            case "orange":
                resistTot = "3";
                break;
            case "yellow":
                resistTot = "4";
                break;
            case "green":
                resistTot = "5";
                break;
            case "blue":
                resistTot = "6";
                break;
            case "violet":
                resistTot = "7";
                break;
            case "gray":
                resistTot = "8";
                break;
            case "white":
                resistTot = "9";
                break;
            default:
                break;
        }
        let brand2 = document.getElementById("brand2").style.backgroundColor;
        switch (brand2) {
            case "black":
                resistTot += '0';
                break;
            case "brown":
                resistTot += '1';
                break;
            case "red":
                resistTot += '2';
                break;
            case "orange":
                resistTot += '3';
                break;
            case "yellow":
                resistTot += '4';
                break;
            case "green":
                resistTot += '5';
                break;
            case "blue":
                resistTot += '6';
                break;
            case "violet":
                resistTot += '7';
                break;
            case "gray":
                resistTot += '8';
                break;
            case "white":
                resistTot += '9';
                break;
            default:
                break;
        }
        let brand3 = document.getElementById("brand3").style.backgroundColor;
        switch (brand3) {
            case "brown":
                resistTot += '0';
                break;
            case "red":
                resistTot += "00";
                break;
            case "orange":
                resistTot += "K";
                break;
            case "yellow":
                resistTot += "0K";
                break;
            case "green":
                resistTot += "00K";
                break;
            case "blue":
                resistTot += "M";
                break;
            case "violet":
                resistTot += "0M";
                break;
            case "gray":
                resistTot += "00M";
                break;
            case "white":
                resistTot += "G";
                break;
            case "gold":
                resistTot = (parseInt(resistTot) / 10)
                break;
            case "silver":
                resistTot = (parseInt(resistTot) / 100)
                break;
            default:
                break;
        }
        resistTot += " Ω";
        let brand5 = document.getElementById("brand5").style.backgroundColor;
        if (brand5) {
            switch (brand5) {
                case "brown":
                    resistTot += " ± 1%";
                    break;
                case "red":
                    resistTot += " ± 2%";
                    break;
                case "green":
                    resistTot += " ± 0.5%";
                    break;
                case "blue":
                    resistTot += " ± 0.25%";
                    break;
                case "violet":
                    resistTot += " ± 0.1%";
                    break;
                case "gray":
                    resistTot += " ± 0.05%";
                    break;
                case "gold":
                    resistTot += " ± 5%";
                    break;
                case "silver":
                    resistTot += " ± 0.01%";
                    break;
                default:
                    break;
            }
        }
        document.getElementById("resist-tot").innerText = resistTot;
    }
}

